<script setup>
import { ref, onMounted } from 'vue'
import { useRouter } from 'vue-router'
import { useAuthStore } from '@/stores/auth.js'
import { showError, showWarning } from '@/utils/message.js'
import { getHomeArticles, getSortOperation, getRecommendedArticles, getArticleList } from '@/api/index.js'

const router = useRouter()
const auth = useAuthStore()

const articlesByType = ref({})
const sortInfo = ref([])
const articleCount = ref(0)
const view = ref(0)
const searchMode = ref(true)
const searchInput = ref('')
const searchData = ref([])
const searchArticlecount = ref(0)
const pageSize = ref(5)
const recommendList = ref([])

function getSort(type_id) {
    return sortInfo.value.find(item => item.id.toString() === type_id)?.type
}

function showArticle(id, limit) {
    if (limit === '登录' && !auth.hasLogin()) {
        showError('此文章需要先登录!')
        return
    }
    router.push({ name: 'article', params: { id } })
}

function getArticlesByType(type_id) {
    router.push({ name: 'ArticleOfType', params: { type_id } })
}

function friendEvent() {
    showWarning('友站功能暂未开通!')
}

async function fetchPaginatedArticles(page = 1) {
    const response = await getArticleList({ pageSize: pageSize.value, currentPage: page, title: searchInput.value })
    searchData.value = response.data.articles
    searchArticlecount.value = response.data.count
    searchMode.value = false
}

async function searchButton() {
    try {
        await fetchPaginatedArticles(1)
    } catch (error) {
        showError(error.response?.data?.message || '网络请求失败，请检查网络或稍后重试')
    }
}

async function currentChange(page) {
    try {
        await fetchPaginatedArticles(page)
    } catch (error) {
        showError(error.response?.data?.message || '网络请求失败，请检查网络或稍后重试')
    }
}

onMounted(async () => {
    try {
        const [homeRes, sortRes, recommendRes] = await Promise.all([
            getHomeArticles(),
            getSortOperation(),
            getRecommendedArticles()
        ])

        articlesByType.value = homeRes.data.data
        articleCount.value = homeRes.data.articleCount
        view.value = homeRes.data.view
        sortInfo.value = sortRes.data.types
        recommendList.value = recommendRes.data.data
    } catch (error) {
        showError(error.response?.data?.message || '网络请求失败，请检查网络或稍后重试')
    }
})
</script>


